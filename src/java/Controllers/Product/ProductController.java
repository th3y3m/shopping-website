package Controllers.Product;

import java.io.IOException;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author admin
 */
@WebServlet(name = "ProductController", urlPatterns = {"/ProductController"})
public class ProductController extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    private final String AddProductController = "AddProductController";
    private final String DeleteProductController = "DeleteProductController";
    private final String UpdateProductController = "UpdateProductController";
    private final String ViewProductController = "ViewProductController";
    private final String SearchProductByCategoryController = "SearchProductByCategoryController";
    private final String SearchProductByPriceController = "SearchProductByPriceController";

    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        request.setCharacterEncoding("UTF-8");

        String url = null, action = request.getParameter("action");
        try {
            if (action.equalsIgnoreCase("Add")) {
                url = AddProductController;
            } else if (action.equalsIgnoreCase("Delete")) {
                url = DeleteProductController;
            } else if (action.equalsIgnoreCase("View")) {
                url = ViewProductController;
            } else if (action.equalsIgnoreCase("Update")) {
                url = UpdateProductController;
            } else if (action.equalsIgnoreCase("searchByCategory")) {
                url = SearchProductByCategoryController;
            } else if (action.equalsIgnoreCase("searchByPrice")) {
                url = SearchProductByPriceController;
            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            request.getRequestDispatcher(url).forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
